#include <iostream>
using namespace std;

class SinivelCap // 콧물 처치용 캡슐
{
public:
    void Take() const { cout << "콧물이 싹~ 납니다." << endl; }
};

class SneezeCap // 재채기 처치용 캡슐
{
public:
    void Take() const { cout << "재채기가 멎습니다." << endl; }
};

class SnuffleCap // 코막힘 처치용 캡슐
{
public:
    void Take() const { cout << "코가 뻥 뚫립니다." << endl; }
};

class CONTAC600 // 캡슐화가 되어있는 코 감기약 CONTAC600 클래스
{
private:
    SinivelCap sin;
    SneezeCap sne;
    SnuffleCap snu;

public:
    void Take() const
    {
        sin.Take();
        sne.Take();
        snu.Take();
    }
};

class ColdPatient // 환자 클래스가 간결해졌다.
{
public:
    void TakeCONTAC600(const CONTAC600 &cap) const { cap.Take(); }
};

int main(void)
{
    CONTAC600 cap;
    ColdPatient sufferer;
    // 약먹기가 간결해졌다. => 호출 순서를 신경쓸 필요가 없어졌다!
    sufferer.TakeCONTAC600(cap);
    return 0;
}
